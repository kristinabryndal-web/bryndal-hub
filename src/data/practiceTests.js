// ACT answer letter sets (alternate A-D/E and F-K by question number)
// English/Reading/Science: odd=ABCD, even=FGHJ
// Math: odd=ABCDE, even=FGHJK
export function getChoices(section, qNum) {
  if (section === 'math') {
    return qNum % 2 === 1 ? ['A','B','C','D','E'] : ['F','G','H','J','K']
  }
  return qNum % 2 === 1 ? ['A','B','C','D'] : ['F','G','H','J']
}

export function lookupScaled(section, raw) {
  const table = rawToScaled[section]
  if (!table) return null
  // find the closest key ≤ raw
  const maxRaw = Math.min(raw, Math.max(...Object.keys(table).map(Number)))
  return table[maxRaw] ?? table[Math.max(...Object.keys(table).filter(k => Number(k) <= maxRaw).map(Number))] ?? 1
}
